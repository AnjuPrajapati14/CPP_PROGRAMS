// switch case

#include<iostream>
using namespace std;
int main(){
    char button;
    cin>>button;
    switch(button)
    {
        case 'a':
        cout<<"Hello"<<endl;
        break;
        case 'b':
        cout<<"Hola"<<endl;
        break;
        case 'c':
        cout<<"namaste"<<endl;
        break;
        case 'd':
        cout<<"ram ram"<<endl;
        break;
        case 'e':
        cout<<"hi"<<endl;
        break;
        default:
        cout<<"learning more"<<endl;
        break;

    }

    return 0;
}

